int32_t DequeueMessage_J1939 (uint32_t ctrl, J1939_MESSAGE_T* J1939Msg)
{
    int32_t             result;

    result = RC_SUCCESS;

    if (J1939_CIR_BUF_EMPTY (j1939_RX[ctrl])) {
        result = RC_QUEUEEMPTY;
    } else {
        *J1939Msg = J1939_CIR_BUF_POP (j1939_RX[ctrl]);
    }
    
    return (result);
}

